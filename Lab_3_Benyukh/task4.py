import numpy as np

a = np.array([3, 0, -5, 9, 1])
b = np.array([-2, 1, 6, 2, -4])

print("Масив a:", a)
print("Масив b:", b)

print("\n--- Арифметичні операції ---")
print("Сума a + b:", a + b)
print("Різниця a - b:", a - b)
print("Добуток a * b:", a * b)
if np.any(b == 0):
    print("Ділення a / b: неможливе через наявність нулів у масиві b")
    print("Цілочисельне ділення a // b: неможливе через наявність нулів у масиві b")
    print("Залишок від ділення a % b: неможливе через наявність нулів у масиві b")
else:
    print("Ділення a / b:", a / b)
    print("Цілочисельне ділення a // b:", a // b)
    print("Залишок від ділення a % b:", a % b)

print("Піднесення до степеня a ** 2:", a ** 2)

c = np.concatenate((a, b))
print("\nОб'єднаний масив c:", c)

print("\n--- Статистичні операції ---")
print("Максимальний елемент:", np.max(c))
print("Мінімальний елемент:", np.min(c))
print("Сума всіх елементів:", np.sum(c))
print("Добуток всіх елементів:", np.prod(c))
